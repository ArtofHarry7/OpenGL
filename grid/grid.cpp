glutCuboid box(0, 12, 0, 10, 22, 10);
int jump = 0;
const int jumpTimeConst = 40;
int jumpTime = jumpTimeConst;
int divisor = 10;

void ifForward(float sp){
    if(box.center[0]+5+sp >= freeSpace[0].size())
        return;
    // cout << box.center[0] << " " << box.center[1] << " - " << freeSpace[box.center[1]][box.center[0]+5+sp] << " " << freeSpace[box.center[1]][box.center[0]-5+sp] << "\n";
    if(box.center[1]-5-sp <= 0){
        box.translate(sp, 0, 0);
        box.translate(0, -sp, 0);
        gameOver = true;
        return;
    }
    if(freeSpace[box.center[1]][box.center[0]+5+sp]){
        score++;
        // cout << score << "\n";
        glOrigin.translate(-sp, 0, 0);
        glOrigin.translate(0, 0, sp/3.5);
        box.translate(sp, 0, 0);
    }
    // cout << box.center[0] << " " << box.center[1] << " - " << freeSpace[box.center[1]][box.center[0]+5+sp] << " " << freeSpace[box.center[1]][box.center[0]-5+sp] << "\n";
    if(freeSpace[box.center[1]-5-sp][box.center[0]]){
        box.translate(0, -sp, 0);
    }
    if(jump == 1){
        if(box.center[1]+5+sp*jumpTime/divisor < freeSpace.size() && freeSpace[box.center[1]+5+sp*jumpTime/divisor][box.center[0]]){
            box.translate(0, sp*jumpTime/divisor, 0);
            jumpTime--;
            if(jumpTime == 0){
                // cout << "yaha se\n";
                jump = -1;
            }
        }
        else{
            // cout << "nahi yaha se\n";
            jump = -1;
        }
    }
    else if(jump == -1){
        if(box.center[1]-5-sp*jumpTime/divisor > 0 && freeSpace[box.center[1]-5-sp*jumpTime/divisor][box.center[0]]){
            box.translate(0, -sp*jumpTime/10, 0);
            jumpTime++;
            if(jumpTime == jumpTimeConst){
                jump = 0;
                // cout << "-yaha se\n";
            }
        }
        else{
            // cout << "-nahi yaha se\n";
            jump = 0;
            jumpTime = jumpTimeConst;
        }
    }
}

